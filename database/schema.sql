PROMPT Creating Views for Customer Access...

-- عرض الوجبات النشطة الجاهزة للبيع (ليست وصفة فرعية)
CREATE OR REPLACE VIEW v_active_menu AS
SELECT recipe_id, name, description, selling_price, image_url
FROM recipes
WHERE is_active = 'Y' 
  AND is_sub_recipe = 'N';

-- عرض الخيارات الشائعة للتعديل (يمكن اشتقاقها أو وضعها كخيار ثابت)
CREATE OR REPLACE VIEW v_menu_customizations AS
SELECT DISTINCT customization_name, charge_amount
FROM order_customizations
UNION
SELECT 'إضافة جبنة' as customization_name, 15.00 as charge_amount FROM dual
UNION
SELECT 'بدون بصل' as customization_name, 0.00 as charge_amount FROM dual
UNION
SELECT 'سبايسي (حار)' as customization_name, 0.00 as charge_amount FROM dual;

